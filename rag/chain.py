# -*- coding: utf-8 -*-
"""
RAG-пайплайн: классификация вопроса → (прямой ответ | RAG).
При «Вопрос по ЕСКД» — embedding запроса, retrieval по ChromaDB, генерация ответа LLM.
При «Общий вопрос» — ответ напрямую от LLM без поиска по базе.
"""

from langchain_core.documents import Document
from langchain_core.prompts import ChatPromptTemplate
from langchain_core.runnables import RunnablePassthrough
from langchain_core.output_parsers import StrOutputParser
from langchain_ollama import ChatOllama

import config
from store.chroma_store import get_vectorstore

# Классы вопросов (расширяемо: добавить новые строки и маппинг в classify_question)
QUESTION_CLASS_GENERAL = "Общий вопрос"
QUESTION_CLASS_ESKD = "Вопрос по ЕСКД"

_llm = None


def _get_llm(model_temperature: float) -> ChatOllama:
    """
    Единая инициализация LLM для классификации и генерации.
    """
    global _llm
    if _llm is None:
        _llm = ChatOllama(model=config.LLM_MODEL, temperature=model_temperature, num_predict = 1024)
    return _llm


def classify_question(question: str) -> str:
    """
    Классифицирует вопрос. Возвращает QUESTION_CLASS_GENERAL или QUESTION_CLASS_ESKD.
    Детерминированно (temperature=0), только название класса.
    """
    
    llm = _get_llm(model_temperature = 0)
    prompt = ChatPromptTemplate.from_messages([
        ("system",
     """
Ты — эксперт по классификации технических вопросов инженеров производственного предприятия.

ТВОЯ ЗАДАЧА:
Строго определить, относится ли вопрос к нормативным требованиям ЕСКД/ГОСТ
или носит общий характер.

ДОПУСТИМЫ ТОЛЬКО ДВА КЛАССА:
1. "Вопрос по ЕСКД"
2. "Общий вопрос"

КЛАСС "Вопрос по ЕСКД":

Относится, если:
– упоминаются ГОСТ, ЕСКД или конкретные стандарты;
– спрашивают о правилах оформления чертежей, схем, спецификаций;
– речь идет о требованиях к оформлению документов;
– спрашивают о структуре, обозначениях, шрифтах, масштабах, форматах;
– речь идет об извещениях об изменении, основной надписи, обозначениях;
– требуется соблюдение нормативных требований.

КЛАСС "Общий вопрос":

Относится, если:
– вопрос не связан с нормативными требованиями;
– речь идет об инженерных расчетах, технологиях, материалах;
– вопрос носит общий технический или организационный характер;
– нет упоминаний стандартов или правил оформления.

ПРАВИЛО ПРИ СОМНЕНИИ:

Если в вопросе нет явных признаков нормативных требований или ссылок на стандарты,
выбирай класс "Общий вопрос".

ПРИМЕРЫ:

Вопрос: Как правильно оформить основную надпись на чертеже?
Класс: Вопрос по ЕСКД

Вопрос: Какой материал лучше использовать для изготовления корпуса?
Класс: Общий вопрос

Вопрос: Какие требования ГОСТ предъявляются к масштабу чертежа?
Класс: Вопрос по ЕСКД

Вопрос: Как рассчитать нагрузку на вал?
Класс: Общий вопрос

СТРУКТУРА ОТВЕТА:

1. Ответ должен быть строго на русском языке.
2. Ответ должен состоять строго из одной фразы:
   – "Вопрос по ЕСКД"
   – "Общий вопрос"
3. Не добавлять пояснений.
4. Не использовать точку в конце.
"""),
        ("human",
         """
         Определи к какому классу относится следующий вопрос:\n\n
         
         Вопрос: {question}\n\n
         
         Класс:
         """),
    ])
    chain = prompt | llm | StrOutputParser()
    raw_class = (chain.invoke({"question": question.strip()}) or "").strip()
    raw_class_lower = raw_class.lower()
    if "ескд" in raw_class_lower or "вопрос по ескд" in raw_class_lower:
        return QUESTION_CLASS_ESKD
    return QUESTION_CLASS_GENERAL


def _format_doc(d: Document) -> str:
    """
    Форматирует один документ с метаданными для контекста LLM.
    """
    m = d.metadata
    src = m.get("source", "?")
    page = m.get("page", "?")
    sec = m.get("section", "")
    head = f" [Источник: {src}, стр. {page}]" if sec == "" else f" [{sec} — {src}, стр. {page}]"

    Show_Chunk_Preview = False

    if Show_Chunk_Preview:
        print("\n======= Документы ретривера =======")
        print("Заголовок:", head)
        print("Превью текста:", d.page_content[500:])
        print("\n======= Конец =======")

    return d.page_content.strip() + head


def _format_docs(docs: list[Document]) -> str:
    return "\n\n---\n\n".join(_format_doc(d) for d in docs)


def build_rag_chain():
    """
    Собирает RAG-цепочку: retriever | format_docs | prompt | llm | StrOutputParser.
    Retriever учитывает метаданные через их включение в контекст (source, page, section).
    """
    vs = get_vectorstore()
    retriever = vs.as_retriever(
        search_type="similarity",
        search_kwargs={"k": config.TOP_K},
    )

    prompt = ChatPromptTemplate.from_messages([
        ("system", 
        """
Ты — опытный технический ассистент для инженеров-конструкторов производственного предприятия.
Ты отвечаешь строго на основе предоставленных фрагментов документации ЕСКД.

ТВОЯ ЗАДАЧА:
Дать максимально полный, технически точный и детализированный ответ,
используя ВСЮ релевантную информацию из предоставленных фрагментов.

ОБЩИЕ ПРАВИЛА:

1. ВАЖНО: ОТВЕЧАЙ СТРОГО НА РУССКОМ ЯЗЫКЕ.
2. Не используй иностранные слова (кроме официальных обозначений стандартов).
3. Формулируй текст техническим, деловым стилем.
4. Не допускай повторов.
5. Не добавляй информацию, отсутствующую во фрагментах.
6. Не делай предположений сверх текста.

ПРАВИЛА ПОЛНОТЫ ОТВЕТА:

1. Проанализируй ВСЕ предоставленные фрагменты.
2. Используй информацию из каждого релевантного фрагмента.
3. Если данные распределены по нескольким фрагментам — объедини их в связный технический ответ.
4. Раскрой требования, параметры, ограничения и исключения, если они указаны.
5. Не сокращай технические формулировки, если они важны для точности.
6. Ответ должен быть развернутым и охватывать все аспекты вопроса, отражённые в документации.
7. Ответ должен быть технически подробным и, как правило,содержать не менее 5 содержательных пунктов, если это позволяет документация.

СТРУКТУРА ОТВЕТА:

1. Ответ оформляй в виде нумерованного списка.
2. Количество пунктов определяется содержанием, но ответ не должен быть излишне кратким.
3. Каждый пункт должен быть логически завершённым.
4. Если в тексте есть ссылка на таблицу или рисунок в приложении — обязательно укажи приложение, его номер и документ.

ЕСЛИ ИНФОРМАЦИИ НЕДОСТАТОЧНО:

Напиши строго:
«В предоставленных фрагментах нет информации для ответа на вопрос.»

ПОСЛЕ ОСНОВНОГО ОТВЕТА:

На отдельной строке укажи все использованные источники:
«Источники: документ (стр. N), документ (стр. N)»

ФОРМАТИРОВАНИЕ:

– Без вводных фраз.
– Без рассуждений.
– Без комментариев вне ответа.
– Только техническое содержание.
"""),
        ("human", 
        """
        Фрагменты документации: {context}\n\n

        Вопрос: {question}\n\n

        Ответ (с опорой на источники):\n
        """),
            ])

    llm = _get_llm(model_temperature = 0.1)
    chain = (
        {"context": retriever | _format_docs, "question": RunnablePassthrough()}
        | prompt
        | llm
        | StrOutputParser()
    )
    return chain


def build_direct_chain():
    """Цепочка ответа без RAG: вопрос → LLM → ответ (для «Общий вопрос»)."""
    llm = _get_llm(model_temperature = 0.2)
    prompt = ChatPromptTemplate.from_messages([
        ("system",
         """
Ты — опытный технический ассистент по ГОСТ(государственным стандартам) ЕСКД (Единой системе конструкторской документации) для инженеров производственного предприятия. 

ОБЩИЕ ПРАВИЛА:

1. ВАЖНО: ОТВЕЧАЙ СТРОГО НА РУССКОМ ЯЗЫКЕ.
2. Не допускай повторов предложений или их частей.
3. Формулируй текст техническим, деловым стилем.

СТРУКТУРА ОТВЕТА:

1. Дай полный и связный ответ на вопрос.
2. Предупреди, что вопрос не относится к твоей сфере знаний (ГОСТы и ЕСКД).
3. Предложи задать вопрос по ГОСТ или оформление документации.

ФОРМАТИРОВАНИЕ:

– Без вводных фраз типа «Согласно предоставленному контексту».
– Без рассуждений.
– Без повторов.

         """),
        ("human",
        """
        Вопрос: {question}\n\n
        
        Ответ (с предупреждением о том, что вопрос не относится к теме твоих знаний
        и предложением задать вопрос по ГОСТам):\n

        """),
    ])
    return prompt | llm | StrOutputParser()

def LLM_answer(question: str, chain=None) -> str:
    """
    Классификация вопроса → при «Общий вопрос» ответ напрямую от LLM;
    при «Вопрос по ЕСКД» — RAG (retriever + контекст + LLM).
    chain — опционально уже собранная RAG-цепочка (для обратной совместимости).
    """
    q = (question or "").strip()
    if not q:
        return ""

    cls = classify_question(q)
    print(cls)
    if cls == QUESTION_CLASS_GENERAL:
        chain = build_direct_chain()
        print('Цепочка без RAG построена.')
        return chain.invoke(q)
    elif cls == QUESTION_CLASS_ESKD:
        chain = build_rag_chain()
        print('Цепочка RAG построена.')
        return chain.invoke(q)
    if chain is None:
        chain = build_rag_chain()
    return chain.invoke(q)